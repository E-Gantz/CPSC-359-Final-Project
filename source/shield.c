void writeShield()
{
	int x = state.shieldX;
	int y = state.shieldY;
	x += SHIELDPAD*SIZE;
	for(int s = 0; s < 4; s++)
	{
		switch(shieldArray[s])
		{
			case 1:
			writeShield5(x, y, SHIELDID + s);
			break;
			case 2:
			writeShield4(x, y, SHIELDID + s);
			break;
			case 3:
			writeShield3(x, y, SHIELDID + s);
			break;
			case 4:
			writeShield2(x, y, SHIELDID + s);
			break;
			case 5:
			writeShield1(x, y, SHIELDID + s);
			break;
		}
		x += SHIELDWIDTH*SIZE*3;
	}
}

